package IfElse;

public class ZeroPositive {
    public static void main(String[] args) {
        int A=900;
        if(A<0){
            System.out.println("A is Negative");
        }else if(A==0){
            System.out.println("A is Zero");
        }else {
            System.out.println("A is Positive");

        }
    }
}
